// Arithmetic Operator are the mathematical operations which perform between two numbers

#include <stdio.h>
void main()
{
    int a = 2;
    int b = 3;
    int c = 0;
    c = a + b;
    c = a % b;
    printf("value of a+b is : %d \n", c);

    float d = 20.7;
    float e = 10;
    float g = d + e;
    printf("value of d and e is : %f \n", g);

    int num1 = 50;
    float num2 = 60;
    float result = num1 + num2;
    printf("value of num1 and num2 is : %f", result);
}